// Print numbers from 1 to 10 on the console using a loop.

// using for loop

console.log("Using for loop");

for(let i =1; i <= 5; i++){
    console.log(i);
}

// using while loop

console.log("Using while loop");

let i = 1;

while(i <= 5){
    console.log(i);
    i++;
}

// using do-while loop

console.log("Using do-while loop");

let j = 1;

do{
    console.log(j);
    j++
}
while(j <= 5);

